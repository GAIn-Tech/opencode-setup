#!/usr/bin/env python3
"""
Check node_modules consistency for opencode plugins.
Verifies that installed node_modules match expected plugin structure.
"""

import os
import json
from pathlib import Path


def main():
    """
    Check node_modules directory consistency.

    Returns:
        tuple: (passed: bool, message: str)
    """
    config_dir = Path.home() / ".config" / "opencode"
    node_modules = config_dir / "node_modules"

    # Check node_modules exists
    if not node_modules.exists():
        message = "FAIL: node_modules directory not found at " + str(node_modules)
        return (False, message)

    if not node_modules.is_dir():
        message = "FAIL: node_modules is not a directory at " + str(node_modules)
        return (False, message)

    # Check package.json for expected dependencies
    package_json = config_dir / "package.json"
    if not package_json.exists():
        message = "PASS: No package.json found — skipping dependency check"
        return (True, message)

    try:
        with open(package_json, "r") as f:
            pkg = json.load(f)
    except (json.JSONDecodeError, OSError) as e:
        message = "FAIL: Cannot read package.json — " + str(e)
        return (False, message)

    deps = pkg.get("dependencies", {})
    missing = []

    for dep_name in deps:
        dep_path = node_modules / dep_name
        if not dep_path.exists():
            # Check scoped packages (@org/package)
            if "/" in dep_name:
                parts = dep_name.split("/")
                dep_path = node_modules / parts[0] / parts[1]
            if not dep_path.exists():
                missing.append(dep_name)

    if missing:
        message = "FAIL: Missing node_modules entries: " + ", ".join(missing)
        return (False, message)

    count = len(deps)
    message = f"PASS: All {count} dependencies present in node_modules"
    return (True, message)


if __name__ == "__main__":
    import sys
    passed, message = main()
    print(message)
    sys.exit(0 if passed else 1)
